#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
EVAL_CASES_DIR = ROOT / "evals" / "cases"

# All 13 fields required by run_evals.py REQUIRED_FIELDS + schema
REQUIRED_EVAL_FIELDS = [
    "id",
    "name",
    "module_id",
    "input",
    "expected_status",
    "expected_output_contains",
    "expected_events",
    "expected_steps",
    "expected_checkpoints_min",
    "expected_trace_spans_min",
    "expected_timeline_items_min",
    "expected_tool_calls_min",
    "metadata",
]

NAME_PATTERN = re.compile(r"^[a-z][a-z0-9_]*$")
MAX_NAME_LENGTH = 64

BUSINESS_TERMS: set[str] = {
    "ecommerce", "e_commerce", "电商", "客服", "cs",
    "服装", "clothing", "fashion",
    "订单", "order", "售后", "refund", "return",
    "cad", "灯具", "lighting",
    "报价", "quote", "pricing",
    "自媒体", "social_media", "influencer",
}

SENSITIVE_NAMES: set[str] = {
    "env", ".env", "secret", "secrets",
    "key", "keys", "token", "tokens",
    "credential", "credentials", "password",
    "config", ".config",
}


def validate_name(name: str) -> list[str]:
    errors: list[str] = []
    if not name:
        errors.append("Eval case name is required.")
        return errors
    if len(name) > MAX_NAME_LENGTH:
        errors.append(f"Eval case name too long ({len(name)} > {MAX_NAME_LENGTH} chars).")
    if ".." in name or "/" in name or "\\" in name:
        errors.append(f"Path traversal detected in eval case name: '{name}'")
    if not NAME_PATTERN.fullmatch(name):
        errors.append(
            f"Eval case name must use snake_case (lowercase letters, digits, underscores), "
            f"starting with a letter. Got: '{name}'"
        )
    if name.startswith("."):
        errors.append(f"Eval case name cannot start with '.': '{name}'")
    lower = name.lower()
    if lower in SENSITIVE_NAMES:
        errors.append(f"Eval case name '{name}' is reserved and cannot be used.")
    if lower in BUSINESS_TERMS:
        errors.append(
            f"Eval case name '{name}' contains a business term. "
            f"Eval case names must be business-neutral."
        )
    return errors


def resolve_target_path(name: str) -> Path:
    try:
        target = (EVAL_CASES_DIR / f"{name}.json").resolve()
        resolved_base = EVAL_CASES_DIR.resolve()
    except (OSError, RuntimeError):
        target = (EVAL_CASES_DIR.absolute() / f"{name}.json")
        resolved_base = EVAL_CASES_DIR.absolute()
    try:
        target.relative_to(resolved_base)
    except ValueError:
        raise RuntimeError(
            f"Target path '{target}' is outside evals/cases directory "
            f"'{resolved_base}'. Refusing to proceed."
        )
    return target


def render_eval_case(name: str) -> dict:
    """Generate an eval case dict matching run_evals.py REQUIRED_FIELDS and schema."""
    title = name.replace("_", " ").title()
    return {
        "id": name,
        "name": title,
        "description": f"Neutral eval case for '{name}' generated by Agent Harness Template scaffold.",
        "module_id": "demo_agent",
        "input": f"{name} run input",
        "expected_status": "completed",
        "expected_output_contains": ["demo_agent mock response"],
        "expected_events": ["run.created", "run.started", "run.completed"],
        "expected_steps": ["input_node", "skill_node", "tool_node", "final_node"],
        "expected_checkpoints_min": 4,
        "expected_trace_spans_min": 4,
        "expected_timeline_items_min": 4,
        "expected_tool_calls_min": 1,
        "metadata": {
            "source": "scaffold",
        },
    }


def scaffold_eval_case(name: str, target_path: Path, dry_run: bool = False) -> int:
    if dry_run:
        print(f"Generated file:")
        print(f"  evals/cases/{name}.json")
        print("\n[dry-run] No files were written.")
        return 0

    target_path.parent.mkdir(parents=True, exist_ok=True)
    case = render_eval_case(name)
    target_path.write_text(
        json.dumps(case, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )

    print(f"Generated file:")
    print(f"  evals/cases/{name}.json")
    print(f"\nNext steps:")
    print(f"  - Review: evals/cases/{name}.json")
    print(f"  - Run eval: python3 scripts/run_evals.py")
    return 0


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Scaffold a new eval case.",
    )
    parser.add_argument(
        "--name", "-n", required=True,
        help="Eval case name in snake_case (e.g. my_eval).",
    )
    parser.add_argument("--dry-run", action="store_true",
                        help="Print what would be created without writing files.")
    parser.add_argument("--force", action="store_true",
                        help="Overwrite existing target file.")
    parser.add_argument("--preview", action="store_true",
                        help="Alias for --dry-run.")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    name = args.name.strip()
    dry_run = args.dry_run or args.preview

    errors = validate_name(name)
    if errors:
        for err in errors:
            print(f"Error: {err}", file=sys.stderr)
        return 2

    try:
        target_path = resolve_target_path(name)
    except RuntimeError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 2

    if target_path.exists():
        if not args.force:
            print(
                f"Error: Eval case already exists at {target_path}. "
                f"Use --force to overwrite.",
                file=sys.stderr,
            )
            return 1

    return scaffold_eval_case(name, target_path, dry_run=dry_run)


if __name__ == "__main__":
    raise SystemExit(main())
